    private void listarModalidades() {
        mod.clear();
        List<Modalidade> modalidades = jpa.getModalidades();
        for (Modalidade m : modalidades) {
            mod.addElement(m.getDescricao());
        }
    }

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String modalidadeSelecionada = lstModalidades.getSelectedValue();

        if (modalidadeSelecionada != null) {
            String novaDescricao = JOptionPane.showInputDialog(this, "Digite a nova descrição da modalidade:", modalidadeSelecionada);

            if (novaDescricao != null && !novaDescricao.trim().isEmpty()) {
                try {
                    if (!jpa.conexaoAberta()) {
                        jpa = new PersistenciaJPA();
                    }

                    List<Modalidade> modalidades = jpa.getModalidades();
                    for (Modalidade m : modalidades) {
                        if (m.getDescricao().equals(modalidadeSelecionada)) {
                            m.setDescricao(novaDescricao);
                            jpa.persist(m);
                            break;
                        }
                    }

                    JOptionPane.showMessageDialog(this, "Modalidade editada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro ao editar a modalidade: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
                listarModalidades();
            } else {
                JOptionPane.showMessageDialog(this, "A nova descrição da modalidade não pode ser vazia.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma modalidade para editar.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnEditarActionPerformed

