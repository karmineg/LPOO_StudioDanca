    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:

        String modalidadeSelecionada = lstModalidades.getSelectedValue();

        if (modalidadeSelecionada != null) {
            int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza de que deseja remover a modalidade?", "Confirmação", JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                try {
                    if (!jpa.conexaoAberta()) {
                        jpa = new PersistenciaJPA();
                    }

                    List<Modalidade> modalidades = jpa.getModalidades();
                    for (Modalidade m : modalidades) {
                        if (m.getDescricao().equals(modalidadeSelecionada)) {
                            jpa.remover(m);
                            break;
                        }
                    }

                    JOptionPane.showMessageDialog(this, "Modalidade removida com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro ao remover a modalidade: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
                listarModalidades();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma modalidade para remover.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

